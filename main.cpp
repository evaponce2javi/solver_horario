#include "Menu.h"
#include "Utilidades.h"

int main() {
    configurarConsola();   // UTF-8 en Windows; no-op en Linux/macOS
    limpiarPantalla();
    EstadoApp estado;

    while (true) {
        int opcion = mostrarMenuYLeerOpcion();
        if (!despachar(opcion, estado)) break;
    }
    return 0;
}